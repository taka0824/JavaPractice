
public class WhileExample {
	public static void main(String[] args) {
		System.out.println("------------while-------------");
		int i = 5;
		while(i >= 1){
			System.out.println(i);
			i --;
		}
		System.out.println("------------for-------------");
		for(int j = 5; j > 0; j --) {
			System.out.println(j);
		}
	}
}